package com.napier.sem;

/**
 * Represent a city
 */
public class City {

    /**
     * City ID
     */
    public int id;

    /**
     * City name
     */
    public String name;

    /**
     * City's country code
     */
    public String country_code;

    /**
     * City's district
     */
    public String district;

    /**
     * City's population
     */
    public int population;
}
